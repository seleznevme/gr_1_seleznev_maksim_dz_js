//фунуция разворота строки
function invert (arr) {
	arr = arr.split(''); //превращаю строку в массив
	let arr_inv = []; //будущий развернутый массив
	for(let i = 0; i <= (arr.length - 1); i++){
		arr_inv [i] = arr [(arr.length - 1)-i]; //разворот по индексу
	}
	arr_inv = arr_inv.join(''); //превращаю назад в строку
	return (arr_inv);
}
//проверка
c = invert ('телефон'); //тест
console.log(c);  //выводим результат в консоль

