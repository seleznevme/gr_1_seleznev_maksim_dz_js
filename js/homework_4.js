//функция поиска минимального значения:
function get_min (arr) {
	let min
	for (let i = 0; i <= (arr.length - 1); i++) {
		if (i==0) {
			min = arr[i];
		}
		else if (arr[i] < min) {
		min = arr[i]
		}
	}	
	return (min); //возвращаем результата
}
//проверка
